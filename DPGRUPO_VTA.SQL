SELECT 
 MOV_CODSUC AS GRU_CODSUC, GRU_CODIGO,
 GRU_DESCRI,
 MOV_FECHA       AS GRU_FECHA ,
 SUM(MOV_CANTID) AS GRU_CANTID,
 SUM(MOV_PESO  ) AS GRU_PESO  ,
 SUM(MOV_TOTAL ) AS GRU_TOTAL ,
 SUM(MOV_MTODIV) AS GRU_MTODIV,
 COUNT(*)        AS GRU_COUNT  
 FROM DPMOVINV  INNER JOIN dpinv       ON MOV_CODIGO=INV_CODIGO 
 INNER JOIN dpgru       ON INV_GRUPO =GRU_CODIGO 
 INNER JOIN dptipdoccli ON MOV_TIPDOC=TDC_TIPO AND TDC_ESTVTA=1 
 WHERE MOV_APLORG="V" AND MOV_INVACT=1 
 GROUP BY GRU_CODSUC,INV_GRUPO,MOV_FECHA 
 ORDER BY GRU_CODSUC,INV_GRUPO,MOV_FECHA